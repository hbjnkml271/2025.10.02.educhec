import streamlit as st


st.title("중학교 2학년 2학기 여러가지 사각형들의 성질과 정의")

st.markdown("""
## 개념 소개

중학교 2학년 2학기 수학에서는 다양한 사각형의 정의와 성질을 배웁니다. 아래는 주요 사각형의 정의와 성질입니다.

### 1. 평행사변형
- 두 쌍의 대변이 서로 평행하다.
- 두 쌍의 대변의 길이가 같다.
- 두 쌍의 대각의 크기가 같다.
- 대각선이 서로를 이등분한다.

### 2. 직사각형
- 네 각이 모두 직각이다.
- 대각선의 길이가 같다.
- 평행사변형의 성질을 모두 가진다.

### 3. 마름모
- 네 변의 길이가 모두 같다.
- 대각선이 서로를 수직이등분한다.
- 평행사변형의 성질을 모두 가진다.

### 4. 정사각형
- 네 각이 모두 직각이고, 네 변의 길이가 모두 같다.
- 대각선의 길이가 같다.
- 대각선이 서로를 수직이등분한다.
- 직사각형과 마름모의 성질을 모두 가진다.

### 5. 사다리꼴
- 한 쌍의 대변만 평행하다.

### 6. 등변사다리꼴
- 한 쌍의 대변만 평행하다.
- 다른 두 변의 길이가 같다.
- 대각선의 길이가 같다.

이러한 성질을 바탕으로 사각형의 종류를 구분하고, 문제를 해결할 수 있습니다.
""")
